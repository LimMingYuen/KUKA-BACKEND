        public ActionResult<ApiResponse<WorkflowDiagramPage>> QueryWorkflowDiagrams(QueryWorkflowDiagramsRequest request)
        {
            var page = request.PageNum > 0 ? request.PageNum : 1;
            var size = request.PageSize > 0 ? request.PageSize : Math.Max(WorkflowDiagrams.Count, 1);
            var skip = (page - 1) * size;

            var pagedContent = skip >= WorkflowDiagrams.Count
                ? Array.Empty<WorkflowDiagramDto>()
                : WorkflowDiagrams.Skip(skip).Take(size).ToArray();

            var response = new ApiResponse<WorkflowDiagramPage>
            {
                Code = 0,
                Msg = "SUCCESS",
                Succ = true,
                Data = new WorkflowDiagramPage
                {
                    TotalPages = (int)Math.Ceiling(WorkflowDiagrams.Count / (double)size),
                    TotalRecords = WorkflowDiagrams.Count,
                    Page = page,
                    Size = size,
                    Content = pagedContent
                }
            };

            return Ok(response);
        }

